# Tipo de dado bool(boolean)
# Ao questional algo em um programa,
# só existe duas respostas apossíveis:
# sim(True) ou não (False).
# Existem vários operados para "questionar".
# Dentre eles o ==, que é um operardor lógico que
# questiona se um valor é igual a outro

print(10 == 10) # Sim => True(verdadeiro)
print(10 == 11) # Não => False(Falso)
print(type(10 == 10))
print(type(10 == 11))
